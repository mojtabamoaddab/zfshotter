SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
ZFSHOTTER_ROOT="$(dirname "$SCRIPT_DIR")"

export LOG_DIR=/var/log/zfshotter

[[ -d "$LOG_DIR" ]] || mkdir -p "$LOG_DIR"

source "$ZFSHOTTER_ROOT/lib/module_loader.sh"

load_module config_loader

CRON_FILE="/etc/cron.d/zfshotter"

cat > "$CRON_FILE" << EOF
#
# DO NOT EDIT THIS FILE
#
# It is automatically generated with mkcron.sh
#

EOF

for job in "$ZFSHOTTER_JOBS_DIR"/*.conf
do
    "$SCRIPT_DIR/internal/cron.sh" "$job" >> "$CRON_FILE"
done
